package com.scia.converter.exception;

public class SciaXMLConverterException extends Exception {

	private static final long serialVersionUID = 1L;

	public SciaXMLConverterException() {
	}

	public SciaXMLConverterException(String arg0) {
		super(arg0);
	}

	public SciaXMLConverterException(Throwable arg0) {
		super(arg0);
	}

	public SciaXMLConverterException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
